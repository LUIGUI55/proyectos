// crearemos una clase llamada Calculadora
// que tendrá un método llamado Sumar
// que recibirá dos números y devolverá la suma de ambos
// y un método llamado Restar
// que recibirá dos números y devolverá la resta de ambos

using System;
class Calculadora
{
    public int Sumar(int a, int b)
    {
        return a + b;
    }

    public int Restar(int a, int b)
    {
        return a - b;
    }
}
